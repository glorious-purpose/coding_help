    def test_0(self):
        test = ["bread"], [["yeast", "flour"]], ["yeast", "flour", "corn"]
        ans = ["bread"]
        self.assertCountEqual(self.func(*test), ans)

    def test_1(self):
        test = ["bread", "sandwich"], [["yeast", "flour"], ["bread", "meat"]], ["yeast", "flour", "meat"]
        ans = ["bread", "sandwich"]
        self.assertCountEqual(self.func(*test), ans)

    def test_2(self):
        test = ["bread", "sandwich", "burger"], [["yeast", "flour"], ["bread", "meat"], ["sandwich", "meat", "bread"]], ["yeast", "flour", "meat"]
